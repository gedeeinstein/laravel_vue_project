<script> @minify
// --------------------------------------------------------------------------
(function( $, io, document, window, undefined ){
    // ----------------------------------------------------------------------

    // ----------------------------------------------------------------------
    // Contract real estate
    // ----------------------------------------------------------------------
    Vue.component( 'important-note-city-planning-restriction', {
        // ------------------------------------------------------------------
        props: [ 'value', 'purchase_sale', 'disabled' ],
        template: '#important-note-city-planning-restriction',
        // ------------------------------------------------------------------

        // ------------------------------------------------------------------
        // Reactive data
        // ------------------------------------------------------------------
        data: function(){
            // --------------------------------------------------------------
            var data = {
                entry: this.value
            };
            // --------------------------------------------------------------

            // --------------------------------------------------------------
            return data;
            // --------------------------------------------------------------
        },
        // ------------------------------------------------------------------

        // ------------------------------------------------------------------
        // Computed properties
        // ------------------------------------------------------------------
        computed: {
            // --------------------------------------------------------------
            // Find out if this section is disabled
            // --------------------------------------------------------------
            isDisabled: function(){
                return this.disabled || false;
            },
            // --------------------------------------------------------------

            // --------------------------------------------------------------
            // Find out if this group is completed
            // --------------------------------------------------------------
            isCompleted: function(){
                var entry = this.entry;
                return entry.restricted_law_status == true || false;
            },
            // --------------------------------------------------------------

            // -----------------------------------------------------------------
            // Get city planning facility value
            // -----------------------------------------------------------------
            city_planning_facility: {
                get: function(){
                    return this.entry.city_planning_facility
                },
                set: function(value){
                    Vue.set(this.entry, 'city_planning_facility', value);

                    if (value == 2 || !value){
                        Vue.set(this.entry, 'city_planning_facility_possession', null);
                        Vue.set(this.entry, 'city_planning_facility_possession_road', null);
                    }
                }
            },
            // -----------------------------------------------------------------

            // -----------------------------------------------------------------
            // Get city planning facility possesion value
            // -----------------------------------------------------------------
            city_planning_facility_possession: {
                get: function(){
                    return this.entry.city_planning_facility_possession
                },
                set: function(value){
                    Vue.set(this.entry, 'city_planning_facility_possession', value);

                    if (value == 2 || !value)
                        Vue.set(this.entry, 'city_planning_facility_possession_road', null);
                }
            },
            // -----------------------------------------------------------------
        },
        // ------------------------------------------------------------------

        // ------------------------------------------------------------------
        // Watchers
        // ------------------------------------------------------------------
        watch: {
            // --------------------------------------------------------------

            // --------------------------------------------------------------
        },
        // ------------------------------------------------------------------

        // ------------------------------------------------------------------
        // Component methods
        // ------------------------------------------------------------------
        methods: {

        }
        // ------------------------------------------------------------------
    });
    // ----------------------------------------------------------------------
}( jQuery, _, document, window ));
// --------------------------------------------------------------------------
@endminify </script>

<script> @minify
// --------------------------------------------------------------------------
(function( $, io, document, window, undefined ){
    // ----------------------------------------------------------------------

    // ----------------------------------------------------------------------
    // Contract real estate
    // ----------------------------------------------------------------------
    Vue.component( 'contract-real-estate', {
        // ------------------------------------------------------------------
        props: [ 'value', 'project', 'contract', 'target', 'buildings', 'disabled' ],
        template: '#contract-real-estate',
        // ------------------------------------------------------------------

        // ------------------------------------------------------------------
        // Reactive data
        // ------------------------------------------------------------------
        data: function(){
            // --------------------------------------------------------------
            var data = {
                entry: this.value,
                project: this.project,
            };
            // --------------------------------------------------------------

            // --------------------------------------------------------------
            // console.log( this.buildings );
            return data;
            // --------------------------------------------------------------
        },
        // ------------------------------------------------------------------

        // ------------------------------------------------------------------
        // Computed properties
        // ------------------------------------------------------------------
        computed: {
            // --------------------------------------------------------------
            prefix: function(){
                var entry = this.entry;
                var prefix = 'new-contract-real-estate-';
                return entry.id ? 'contract-real-estate-' +entry.id+ '-' : prefix;
            },
            // --------------------------------------------------------------

            // --------------------------------------------------------------
            // Find out if this section is disabled
            // --------------------------------------------------------------
            isDisabled: function(){
                return this.disabled || this.isCompleted;
            },
            // --------------------------------------------------------------

            // --------------------------------------------------------------
            // Find out if this group is completed
            // --------------------------------------------------------------
            isCompleted: function(){
                return 1 === this.entry.notices_status;
            },
            // --------------------------------------------------------------

            // --------------------------------------------------------------
            // Get single building object
            // --------------------------------------------------------------
            building: function(){
                var result = {};
                var buildings = io.get( this.project, 'property.buildings' );
                if( buildings && buildings.length ) result = buildings[0];
                return result;
            },
            // --------------------------------------------------------------

            // --------------------------------------------------------------
            // If the project has residentials, buildings or roads
            // --------------------------------------------------------------
            hasRoads: function(){ return io.get( this, 'project.property.roads' )},
            hasBuildings: function(){ return io.get( this, 'project.property.buildings' )},
            hasResidentials: function(){ return io.get( this, 'project.property.residentials' )},
            // --------------------------------------------------------------
        },
        // ------------------------------------------------------------------

        // ------------------------------------------------------------------
        // Watchers
        // ------------------------------------------------------------------
        watch: {
            // --------------------------------------------------------------
            
            // --------------------------------------------------------------
        },
        // ------------------------------------------------------------------

        // ------------------------------------------------------------------
        // Component methods
        // ------------------------------------------------------------------
        methods: {
            
        }
        // ------------------------------------------------------------------
    });
    // ----------------------------------------------------------------------
}( jQuery, _, document, window ));
// --------------------------------------------------------------------------
@endminify </script>
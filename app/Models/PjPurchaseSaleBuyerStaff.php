<?php
// --------------------------------------------------------------------------
namespace App\Models;
use Illuminate\Database\Eloquent\Model;
// --------------------------------------------------------------------------
use App\Models\User;
use App\Models\PjPurchaseSale as PurchaseSale;
// --------------------------------------------------------------------------

// --------------------------------------------------------------------------
class PjPurchaseSaleBuyerStaff extends Model {
    // ----------------------------------------------------------------------
    protected $table = 'pj_purchase_sale_buyer_staffs';
    // ----------------------------------------------------------------------
	protected $fillable = [
		'user_id',
		'pj_purchase_sale_id',
    ];
    // ----------------------------------------------------------------------
    
    // ----------------------------------------------------------------------
	public function purchaseSale(){
		return $this->belongsTo( PurchaseSale::class );
    }
    // ----------------------------------------------------------------------

    // ----------------------------------------------------------------------
	public function user(){
		return $this->belongsTo( User::class );
    }
    // ----------------------------------------------------------------------	
}
// --------------------------------------------------------------------------

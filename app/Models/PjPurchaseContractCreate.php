<?php
// --------------------------------------------------------------------------
namespace App\Models;
use Illuminate\Database\Eloquent\Model;
// --------------------------------------------------------------------------
use App\Models\PjPurchaseContract as Contract;
use App\Models\PjPurchaseContractCreateBuilding as Building;
// --------------------------------------------------------------------------

// --------------------------------------------------------------------------
class PjPurchaseContractCreate extends Model {
    // ----------------------------------------------------------------------
    protected $guarded = array( 'id' );
    // ----------------------------------------------------------------------

    // ----------------------------------------------------------------------
    public function contract(){
        return $this->belongsTo( Contract::class, 'pj_purchase_contract_id' );
    }
    // ----------------------------------------------------------------------

    // ----------------------------------------------------------------------
    public function buildings(){
        return $this->hasMany( Building::class );
    }
    // ----------------------------------------------------------------------
}
// --------------------------------------------------------------------------

<?php
// --------------------------------------------------------------------------
namespace App\Models;
use Illuminate\Database\Eloquent\Model;
// --------------------------------------------------------------------------

// --------------------------------------------------------------------------
class MasterValue extends Model {
    // ----------------------------------------------------------------------
    protected $appends = [ 'alt' ];
    protected $fillable = [
        'type',
        'key',
        'sort',
        'value',
        'masterdeleted'
    ];
    // ----------------------------------------------------------------------

    // ----------------------------------------------------------------------
    public function getAltAttribute(){
        if( is_numeric( $this->key )) return (int) $this->key;
        return $this->key;
    }
    // ----------------------------------------------------------------------
}
// --------------------------------------------------------------------------

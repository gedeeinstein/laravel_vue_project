<?php
// --------------------------------------------------------------------------
namespace App\Models;
use Illuminate\Database\Eloquent\Model;
// --------------------------------------------------------------------------
use App\Models\User as User;
// --------------------------------------------------------------------------

// --------------------------------------------------------------------------
class UserRole extends Model {
    // ----------------------------------------------------------------------
    public $timestamps = false;
    // ----------------------------------------------------------------------
    protected $fillable = [
        'display_name',
        'label'
    ];
    // ----------------------------------------------------------------------

    // ----------------------------------------------------------------------
    public function user(){
        return $this->hasMany( User::class );
    }
    // ----------------------------------------------------------------------
}
// --------------------------------------------------------------------------

<?php
// --------------------------------------------------------------------------
namespace App\Models;
// --------------------------------------------------------------------------
use Illuminate\Database\Eloquent\Model;
use Illuminate\Database\Eloquent\SoftDeletes;
// --------------------------------------------------------------------------
use App\Models\PjMemo as Memo;
use App\Models\PjPurchaseSale as PurchaseSale;
// --------------------------------------------------------------------------


// --------------------------------------------------------------------------
class PjPurchaseSalePjMemo extends Model {
    // ----------------------------------------------------------------------
    use SoftDeletes;
    // ----------------------------------------------------------------------
    protected $table = 'pj_purchase_sale_pj_memos';
    // ----------------------------------------------------------------------
	protected $fillable = [
		'content',
		'pj_purchase_sale_id',
    ];
    // ----------------------------------------------------------------------

    // ----------------------------------------------------------------------
	public function purchaseSale(){
		return $this->belongsTo( PurchaseSale::class );
    }
    // ----------------------------------------------------------------------

    // ----------------------------------------------------------------------
    public function memo(){
        return $this->belongsTo( Memo::class, 'pj_memo_id' );
    }
    // ----------------------------------------------------------------------
}
// --------------------------------------------------------------------------

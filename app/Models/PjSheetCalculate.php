<?php
// --------------------------------------------------------------------------
namespace App\Models;
use Illuminate\Database\Eloquent\Model;
// --------------------------------------------------------------------------
use App\Models\MasterValue;
// --------------------------------------------------------------------------

// --------------------------------------------------------------------------
class PjSheetCalculate extends Model {
    // ----------------------------------------------------------------------
    protected $table = 'pj_sheet_calculates';
    // ----------------------------------------------------------------------
    protected $fillable = [
        'field_id', 'value'
    ];
    // ----------------------------------------------------------------------

    // ----------------------------------------------------------------------
    // Master value relation
    // ----------------------------------------------------------------------
    public function master(){
        return $this->belongsTo( MasterValue::class, 'field_id' );
    }
    // ----------------------------------------------------------------------
}

    // ----------------------------------------------------------------------
    $stamps = collect([ 
        200, 400, 600, 1000, 2000, 4000, 6000, 10000, 20000, 
        40000, 60000, 100000, 150000, 200000
    ]);
    // ----------------------------------------------------------------------

    // ----------------------------------------------------------------------
    $result->pj_stocking_id = 1;
    $result->pj_stock_cost_parent_id = 1;
    // ----------------------------------------------------------------------
    $result->total_interest_rate = $faker->numberBetween( 100000, 1000000 );
    $result->expected_interest_rate = $faker->numberBetween( 100000, 1000000 );
    // ----------------------------------------------------------------------
    $result->banking_fee = 50000;
    $result->banking_fee_memo = $faker->text( 128 );
    // ----------------------------------------------------------------------
    $result->stamp = $stamps->random();

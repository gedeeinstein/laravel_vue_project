<?php
/** @var \Illuminate\Database\Eloquent\Factory $factory */
// --------------------------------------------------------------------------
use Carbon\Carbon;
use Faker\Factory as Faker;
// --------------------------------------------------------------------------

// --------------------------------------------------------------------------
$factory->define( App\Models\PjStockProcurement::class, function(){
    // ----------------------------------------------------------------------
    $result = new \stdClass;
    $faker = Faker::create('ja_JP');
    // ----------------------------------------------------------------------
    $result->pj_stocking_id = 1;
    // ----------------------------------------------------------------------
    $result->price = $faker->numberBetween( 4000000, 90000000 );
    $result->price_tsubo_unit = $faker->numberBetween( 40000, 300000 );
    // ----------------------------------------------------------------------
    $result->brokerage_fee = $faker->numberBetween( 40000, 300000 );
    $result->brokerage_fee_type = $faker->numberBetween( 1, 3 );
    $result->brokerage_fee_memo = $faker->text( 128 );
    // ----------------------------------------------------------------------
    if( 1 === $result->brokerage_fee_type ){
        $result->brokerage_fee = $result->brokerage_fee * -1;
    }
    // ----------------------------------------------------------------------
    $result->created_at = Carbon::now();
    $result->updated_at = Carbon::now();
    // ----------------------------------------------------------------------
    return (array) $result;
    // ----------------------------------------------------------------------
});
// --------------------------------------------------------------------------


// --------------------------------------------------------------------------
$factory->state( App\Models\PjStockProcurement::class, 'init', function(){
    // ----------------------------------------------------------------------
    $result = new \stdClass;
    // ----------------------------------------------------------------------
    $result->pj_stocking_id = null;
    // ----------------------------------------------------------------------
    $result->price = null;
    $result->price_tsubo_unit = null;
    // ----------------------------------------------------------------------
    $result->brokerage_fee = null;
    $result->brokerage_fee_type = 2;
    $result->brokerage_fee_memo = null;
    // ----------------------------------------------------------------------
    $result->created_at = null;
    $result->updated_at = null;
    // ----------------------------------------------------------------------
    return (array) $result;
    // ----------------------------------------------------------------------
});
// --------------------------------------------------------------------------
